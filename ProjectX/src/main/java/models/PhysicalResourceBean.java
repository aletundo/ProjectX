package models;

public class PhysicalResourceBean {
	
	private int idPhysicalResource;
	private String type;
	private int quantity;
	public int getIdPhysicalResource() {
		return idPhysicalResource;
	}
	public void setIdPhysicalResource(int idPhysicalResource) {
		this.idPhysicalResource = idPhysicalResource;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
	

}
